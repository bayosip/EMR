public class PatientEmployer{

        private String pOccupation, pIndustry, pEmployer, pEmployAddress;

        public PatientEmployer (){
        }

    public String getpOccupation() {
        return pOccupation;
    }

    public void setpOccupation(String pOccupation) {
        this.pOccupation = pOccupation;
    }

    public String getpIndustry() {
        return pIndustry;
    }

    public void setpIndustry(String pIndustry) {
        this.pIndustry = pIndustry;
    }

    public String getpEmployer() {
        return pEmployer;
    }

    public void setpEmployer(String pEmployer) {
        this.pEmployer = pEmployer;
    }

    public String getpEmployAddress() {
        return pEmployAddress;
    }

    public void setpEmployAddress(String pEmployAddress) {
        this.pEmployAddress = pEmployAddress;
    }
}
