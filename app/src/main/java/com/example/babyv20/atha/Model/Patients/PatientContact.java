public class PatientContact {


    private String pAddress, pEmgContact, pEmgPhone,
                    pPhone, pMobile, pEmail;
    public PatientContact (){
    }

    public String getpAddress() {
        return pAddress;
    }

    public void setpAddress(String pAddress) {
        this.pAddress = pAddress;
    }


    public String getpEmgContact() {
        return pEmgContact;
    }

    public void setpEmgContact(String pEmgContact) {
        this.pEmgContact = pEmgContact;
    }

    public String getpEmgPhone() {
        return pEmgPhone;
    }

    public void setpEmgPhone(String pEmgPhone) {
        this.pEmgPhone = pEmgPhone;
    }

    public String getpPhone() {
        return pPhone;
    }

    public void setpPhone(String pPhone) {
        this.pPhone = pPhone;
    }

    public String getpMobile() {
        return pMobile;
    }

    public void setpMobile(String pMobile) {
        this.pMobile = pMobile;
    }

    public String getpEmail() {
        return pEmail;
    }

    public void setpEmail(String pEmail) {
        this.pEmail = pEmail;
    }
}
