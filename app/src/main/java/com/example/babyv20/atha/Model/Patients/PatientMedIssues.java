public class PatientMedIssues {

    private String pName, issType, issSpecifics,
            issBeginDate, issEndDate, issOccurrence, issReferrals, issOutcome;

    public String getpName() {
        return pName;
    }

    public void setpName(String pName) {
        this.pName = pName;
    }

    public String getIssType() {
        return issType;
    }

    public void setIssType(String issType) {
        this.issType = issType;
    }

    public String getIssSpecifics() {
        return issSpecifics;
    }

    public void setIssSpecifics(String issSpecifics) {
        this.issSpecifics = issSpecifics;
    }

    public String getIssBeginDate() {
        return issBeginDate;
    }

    public void setIssBeginDate(String issBeginDate) {
        this.issBeginDate = issBeginDate;
    }

    public String getIssEndDate() {
        return issEndDate;
    }

    public void setIssEndDate(String issEndDate) {
        this.issEndDate = issEndDate;
    }

    public String getIssOccurrence() {
        return issOccurrence;
    }

    public void setIssOccurrence(String issOccurrence) {
        this.issOccurrence = issOccurrence;
    }

    public String getIssReferrals() {
        return issReferrals;
    }

    public void setIssReferrals(String issReferrals) {
        this.issReferrals = issReferrals;
    }

    public String getIssOutcome() {
        return issOutcome;
    }

    public void setIssOutcome(String issOutcome) {
        this.issOutcome = issOutcome;
    }
}
